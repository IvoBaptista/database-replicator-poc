CREATE TABLE PRODUCT (
	WAREHOUSE VARCHAR(15),
	BAR_CODE VARCHAR(30),
	DESIGNATION VARCHAR(200),
	CATEGORY VARCHAR(100),
	SUB_CATEGORY VARCHAR(100),
	PRICE DECIMAL(18, 4),
	STOCK int,
	REFS VARCHAR(2000)
);

CREATE INDEX ix_barcode ON PRODUCT (
	BAR_CODE
);

